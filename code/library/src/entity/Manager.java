package entity;


public class Manager {

  private String id;
  private String pwd;
  private String name;
  private String ago;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getPwd() {
    return pwd;
  }

  public void setPwd(String pwd) {
    this.pwd = pwd;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getAgo() {
    return ago;
  }

  public void setAgo(String ago) {
    this.ago = ago;
  }

}
